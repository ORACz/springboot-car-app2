package com.springbootshopapp.convertColor;


import com.springbootshopapp.exception.ColorNotFound;
import com.springbootshopapp.model.Color;
import org.springframework.stereotype.Component;

import java.util.EnumSet;

@Component
public class ConvertColor {

    public Color convertToEnum(String color) {
        return EnumSet.allOf(Color.class).stream()
                .filter(color1 -> color1.name().equalsIgnoreCase(color))
                .findAny().orElseThrow(() -> new ColorNotFound(color));
    }
}


























































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































